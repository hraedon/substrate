class SubstrateError(Exception):
    def __init__(self, code: str, message: str, detail: dict | None = None) -> None:
        self.code = code
        self.message = message
        self.detail = detail
        super().__init__(str(self))

    def __str__(self) -> str:
        return f"[{self.code}] {self.message}"
